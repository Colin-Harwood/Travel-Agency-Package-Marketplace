DROP DATABASE IF EXISTS tripistry_data;
CREATE DATABASE tripistry_data CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE tripistry_data;

CREATE TABLE USER (
    userID          INT AUTO_INCREMENT,
    firstName       VARCHAR(50)  NOT NULL,
    middleName      VARCHAR(50),
    lastName        VARCHAR(50)  NOT NULL,
    emailAddress    VARCHAR(100) NOT NULL UNIQUE,
    phoneNumber     VARCHAR(20)  NOT NULL,
    password        VARCHAR(255) NOT NULL,
    salt            VARCHAR(255) NOT NULL,
    apiKey          VARCHAR(255) NOT NULL,
    userType        ENUM('Traveller', 'Agency') NOT NULL,
    PRIMARY KEY (userID)
);
CREATE TABLE TRAVELLER (
    userID      INT,
    paymentID   VARCHAR(100),
    PRIMARY KEY (userID),
    FOREIGN KEY (userID) REFERENCES USER(userID) ON DELETE CASCADE
);
CREATE TABLE TRAVEL_AGENCY (
    userID      INT,
    name        VARCHAR(100) NOT NULL,
    street      VARCHAR(100),
    suburb      VARCHAR(100),
    city        VARCHAR(100),
    country     VARCHAR(100),
    PRIMARY KEY (userID),
    FOREIGN KEY (userID) REFERENCES USER(userID) ON DELETE CASCADE
);
CREATE TABLE DESTINATION (
    destinationID   INT AUTO_INCREMENT,
    country         VARCHAR(100) NOT NULL,
    city            VARCHAR(100) NOT NULL,
    description     TEXT,
    PRIMARY KEY (destinationID)
);
CREATE TABLE PACKAGE (
    packageID       INT AUTO_INCREMENT,
    type            ENUM('Adventure','Cultural','Beach','City','Safari','Ski','Cruise','Wellness') NOT NULL,
    name            VARCHAR(150) NOT NULL,
    description     TEXT,
    pricePerPerson  DECIMAL(10,2) NOT NULL CHECK (pricePerPerson > 0),
    status          ENUM('Active','Inactive','Draft') NOT NULL DEFAULT 'Draft',
    duration        INT NOT NULL CHECK (duration > 0),
    destinationID   INT NOT NULL,
    agencyID        INT NOT NULL,
    PRIMARY KEY (packageID),
    FOREIGN KEY (destinationID) REFERENCES DESTINATION(destinationID),
    FOREIGN KEY (agencyID)      REFERENCES TRAVEL_AGENCY(userID)
);
CREATE TABLE GROUP_TRIP (
    tripID      INT AUTO_INCREMENT,
    maxSize     INT NOT NULL CHECK (maxSize > 0),
    tripDate    DATE NOT NULL,
    currentSize INT NOT NULL DEFAULT 0 CHECK (currentSize >= 0),
    packageID   INT NOT NULL,
    agencyID    INT NOT NULL,
    PRIMARY KEY (tripID),
    FOREIGN KEY (packageID) REFERENCES PACKAGE(packageID),
    FOREIGN KEY (agencyID)  REFERENCES TRAVEL_AGENCY(userID)
);
CREATE TABLE FLIGHT (
    flightID            INT AUTO_INCREMENT,
    airline             VARCHAR(100) NOT NULL,
    flightNumber        VARCHAR(20)  NOT NULL,
    departureAirport    VARCHAR(100) NOT NULL,
    arrivalAirport      VARCHAR(100) NOT NULL,
    departureTime       DATETIME     NOT NULL,
    arrivalTime         DATETIME     NOT NULL,
    price               DECIMAL(10,2) NOT NULL CHECK (price > 0),
    PRIMARY KEY (flightID)
);
CREATE TABLE ACCOMMODATION (
    accommodationID INT AUTO_INCREMENT,
    name            VARCHAR(150) NOT NULL,
    type            ENUM('Hotel','Hostel','Resort','Apartment','Villa','Guesthouse') NOT NULL,
    rating          DECIMAL(2,1) CHECK (rating BETWEEN 0 AND 5),
    pricePerNight   DECIMAL(10,2) NOT NULL CHECK (pricePerNight > 0),
    destinationID   INT NOT NULL,
    PRIMARY KEY (accommodationID),
    FOREIGN KEY (destinationID) REFERENCES DESTINATION(destinationID)
);
CREATE TABLE RESTAURANT (
    restaurantID    INT AUTO_INCREMENT,
    name            VARCHAR(150) NOT NULL,
    priceRange      ENUM('$','$$','$$$','$$$$') NOT NULL,
    cuisine         VARCHAR(100),
    destinationID   INT NOT NULL,
    PRIMARY KEY (restaurantID),
    FOREIGN KEY (destinationID) REFERENCES DESTINATION(destinationID)
);
CREATE TABLE ATTRACTION (
    name            VARCHAR(150),
    entryFee        DECIMAL(10,2) DEFAULT 0.00,
    description     TEXT,
    destinationID   INT NOT NULL,
    PRIMARY KEY (destinationID, name),
    FOREIGN KEY (destinationID) REFERENCES DESTINATION(destinationID)
);
CREATE TABLE `ORDER` (
    orderID         INT AUTO_INCREMENT,
    startDate       DATE NOT NULL,
    numTravellers   INT NOT NULL CHECK (numTravellers > 0),
    status          ENUM('Pending','Confirmed','Cancelled','Completed') NOT NULL DEFAULT 'Pending',
    totalPrice      DECIMAL(10,2) NOT NULL CHECK (totalPrice > 0),
    userID          INT NOT NULL,
    packageID       INT NOT NULL,
    PRIMARY KEY (orderID),
    FOREIGN KEY (userID)    REFERENCES TRAVELLER(userID),
    FOREIGN KEY (packageID) REFERENCES PACKAGE(packageID)
);
CREATE TABLE REVIEW (
    reviewID    INT AUTO_INCREMENT,
    comment     TEXT,
    starRating  TINYINT NOT NULL CHECK (starRating BETWEEN 1 AND 5),
    reviewDate  DATE NOT NULL,
    userID      INT NOT NULL,
    packageID   INT NOT NULL,
    agencyID    INT,
    PRIMARY KEY (reviewID),
    FOREIGN KEY (userID)    REFERENCES TRAVELLER(userID),
    FOREIGN KEY (packageID) REFERENCES PACKAGE(packageID)
);
CREATE TABLE TRAVELLER_GROUP_TRIP (
    userID      INT,
    tripID      INT,
    joinDate    DATE NOT NULL,
    PRIMARY KEY (userID, tripID),
    FOREIGN KEY (userID)  REFERENCES TRAVELLER(userID),
    FOREIGN KEY (tripID)  REFERENCES GROUP_TRIP(tripID)
);
CREATE TABLE PACKAGE_ATTRACTION (
    packageID       INT,
    destinationID   INT,
    name            VARCHAR(150),
    PRIMARY KEY (packageID, destinationID, name),
    FOREIGN KEY (packageID)           REFERENCES PACKAGE(packageID),
    FOREIGN KEY (destinationID, name) REFERENCES ATTRACTION(destinationID, name)
);
CREATE TABLE PACKAGE_RESTAURANT (
    packageID       INT,
    restaurantID    INT,
    PRIMARY KEY (packageID, restaurantID),
    FOREIGN KEY (packageID)    REFERENCES PACKAGE(packageID),
    FOREIGN KEY (restaurantID) REFERENCES RESTAURANT(restaurantID)
);
CREATE TABLE PACKAGE_FLIGHT (
    packageID   INT,
    flightID    INT,
    PRIMARY KEY (packageID, flightID),
    FOREIGN KEY (packageID) REFERENCES PACKAGE(packageID),
    FOREIGN KEY (flightID)  REFERENCES FLIGHT(flightID)
);
CREATE TABLE PACKAGE_ACCOMMODATION (
    packageID       INT,
    accommodationID INT,
    PRIMARY KEY (packageID, accommodationID),
    FOREIGN KEY (packageID)       REFERENCES PACKAGE(packageID),
    FOREIGN KEY (accommodationID) REFERENCES ACCOMMODATION(accommodationID)
);

